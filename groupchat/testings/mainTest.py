#This function adds the two numbers
def add(x, y):
    return (x + y)

#This function subtracts the two numbers
def subtract(x, y):
    return (x - y)

#This function multiplies the two numbers
def multiply(x, y):
    return (x * y)

#This function divides the two numbers
def divide(x, y):
    return (x / y)
